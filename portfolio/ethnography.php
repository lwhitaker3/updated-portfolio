              <div id="step2" class="step col-md-11 col-md-offset-1">
                <p class="headerline">2. Planning</p>
                <h4 class="color">Developing the Field Guide</h4>
                <div class="row">
                  <div class="col-xs-2 col-md-1">
                    <div class="lineBreak project-steps"></div>
                  </div>
                </div>
                <div class="row">
                  <div class="col-sm-12">
                    <p>We went and observed users interacting with the machine in order to gain a preliminary understanding of potential issues people would have using the machine. It helped us put together a field guide with survey questions we wanted to ask users and the interactions we were looking to observe. </p>
                  </div>
                </div>
                <div class="row">
                  <div class="col-sm-6">
                    <p class="bold">Observation Guide</p>
                    <ol class="larger">
                      <li>How long do people take?</li>
                      <li>What drinks do people get?</li>
                      <li>Is there a variety in drinks/is there a common drink?</li>
                      <li>What problems do people have?</li>
                      <li>Is the interface easy to navigate?</li>
                      <li>Do people take a while to chose a drink?</li>
                      <li>Can people find the ice?</li>
                      <li>Are people using the Coke Freestyle App?</li>
                    </ol>
                  </div>
                  <div class="col-sm-6">
                    <p class="bold">Survey Questions</p>
                    <ol class="larger">
                      <li>What do you like about Coke Freestyle?</li>
                      <li>What would you change about Coke Freestyle?</li>
                      <li>Do you like to experiment with the flavors? What other flavors have you tried?</li>
                      <li>Does this machine make you more/less likely to order a soda at a restaurant? Why?</li>
                      <li>Have you ever downloaded the Coke Freestyle App or followed them on social media?</li>
                      <li>What was your experience?</li>
                    </ol>
                  </div>
