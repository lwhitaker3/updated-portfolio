              <div id="step3" class="step col-md-11 col-md-offset-1">
                <p class="headerline">3. Researching</p>
                <h4 class="color">Our Observations</h4>
                <div class="row">
                  <div class="col-xs-2 col-md-1">
                    <div class="lineBreak project-steps"></div>
                  </div>
                </div>
                <div class="row">
                  <div class="col-sm-12">
                    <img class="portfolio_picture" alt="Notes from Observations" src="/img/coke/notes.jpg">
                  </div>
                  <div class="col-sm-12  margin-top">
                    <ul class="larger">
                      <li>People selected a variety of different drinks:</li>
                        <ul>
                          <li>4 people selected water</li>
                          <li>7 people selected a beverage with no flavoring</li>
                          <li>4 people selected a beverage with flavoring</li>
                        </ul>
                      <li>People spent a variety of different times at the machine:</li>
                        <ul>
                          <li>8 people spent about 10 seconds</li>
                          <li>4 people spent between 10 and 20 seconds</li>
                          <li>4 people spent more than about 30 seconds</li>
                        </ul>
                      <li>Of the 4 people that selected water, 3 of them had trouble locating and dispensing the water</li>
                      <li>8 people got ice, 3 people poured ice out of the cup after dispensing it</li>
                      <li>5 people repeatedly pressed the ‘Push’ button to dispense the beverage to fill it up all the way</li>
                      <li>4 people couldn’t find the ice, one person even turning around to her friend and asking where it was</li>
                      <li>6 people hesitated on pages or went back pages while looking for a drink choice</li>
                      <li>4 people struggled with the touch screen, pressing items multiple times trying to make the screen work</li>
                    </ul>
                  </div>
                </div>
              </div>

              <div id="step4" class="step col-md-11 col-md-offset-1">
                <p class="headerline">4. Findings</p>
                <h4 class="color">Themes and Problem Discovery</h4>
                <div class="row">
                  <div class="col-xs-2 col-md-1">
                    <div class="lineBreak project-steps"></div>
                  </div>
                </div>
                <div class="row">
                  <div class="col-sm-12">
                    <img class="portfolio_picture" alt="Affinity Diagram" src="/img/coke/diagram.jpg">
                  </div>
                </div>
                <div class="row margin-top">
                  <div class="col-sm-12">
                    <p>Based on the affinity diagram we made the main themes that we discovered were:</p>
                  </div>
                </div>

                <div class="row margin-top">
                  <div class="col-sm-6">
                    <p>Inefficiencies</p>
                    <ul class="larger">
                      <li>Not a multi-serve system</li>
                      <li>Takes time for user to make choices</li>
                      <li>Users not understanding how to use the system</li>
                    </ul>
                  </div>
                  <div class="col-sm-6">
                    <p>Variety</p>
                    <ul class="larger">
                      <li>Users selected different drinks</li>
                      <li>Users followed different pattern for selecting drinks</li>
                    </ul>
                  </div>
                </div>

                <div class="row">
                  <div class="col-sm-6">
                    <p>Lack of Intuitive Design</p>
                    <ul class="larger">
                      <li>Buttons are not clear, too small</li>
                      <li>Navigation issues</li>
                      <li>Help button doesn’t make machine easier to use</li>
                      <li>Users were unable to locate ice lever</li>
                    </ul>
                  </div>
                  <div class="col-sm-6">
                    <p>Waste</p>
                    <ul class="larger">
                      <li>Ice poured out fast enough that users threw it away</li>
                      <li>Soda came out too fast</li>
                      <li>Time spent selecting drink</li>
                      <li>Time spent waiting for pages to load on screen</li>
                      <li>Opportunity to promote application</li>
                    </ul>
                  </div>
                </div>


              </div>

              <div id="step5" class="step col-md-11 col-md-offset-1">
                <p class="headerline">5. Redesign</p>
                <h4 class="color">Improving Design Based on Our Results</h4>
                <div class="row">
                  <div class="col-xs-2 col-md-1">
                    <div class="lineBreak project-steps"></div>
                  </div>
                </div>
                <div class="row">
                  <div class="col-sm-12">
                    <p>Based on the research we did we found several opportunities for improvement.</p>
                  </div>
                </div>
                <div class="row margin-top">
                  <div class="col-sm-6">
                    <img class="portfolio_picture" alt="Redesign" src="/img/coke/redesign.jpg">
                  </div>
                  <div class="col-sm-6">
                    <p>The machine should have a separate button for water that is not on the screen in order to make the process of getting water faster. There should be an ice button next to the water button as users were not expecting to press on an ice lever.</p>
                    <p>There should be a barrier where the cup goes so the user can place the cup in the correct place and not spill liquid or ice over their hands.</p>
                    <p>There should be a QR code scanner rather than a QR code on the machine. The scanner should be next to the screen accompanied by a quick line about connecting your phone to the machine, so users are more aware it exists.</p>
                    <p>The interface would be made larger and interactions would be more intuitive.</p>
                  </div>
                </div>

                <div class="row margin-top">
                  <div class="col-sm-6">
                    <img class="portfolio_picture" alt="Annotated Redesign" src="/img/coke/annotated.png">
                  </div>
                  <div class="col-sm-6">
                    <ol class="larger">
                      <li>The user can use the filter to select a subset of drinks, currently, the “ALL” is active. When the user selects another option in the filter at the top of the screen, they will see that subset of drinks</li>
                      <li>The user can see all of the drinks in the category</li>
                      <li>Language toggle</li>
                      <li>When the user selects a beverage the button will expand and flavors becomes visible</li>
                      <li>Once the user chooses a beverage the flavors will appear. The regular version with no flavor is selected by default so the user doesn’t have to select the drink a second time if they just want the plain beverage.</li>
                    </ol>
                  </div>
                </div>

